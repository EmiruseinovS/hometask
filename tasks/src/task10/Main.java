package task10;

public class Main {
    public static void main(String[] args) {
        int x, y;
        x = 3;
        y = 2;
        System.out.println("Сумма переменных равна " + ((x * x) + (y * y)));
        System.out.println("Разность переменных равна " + ((x * x) - (y * y)));
        System.out.println("Произведение переменных равно " + ((x * x) * (y * y)));
        System.out.println("Частное чисел равно " + ((x * x) / (y * y)));
    }
}
