package task11;

public class Main {
    public static void main(String[] args) {
        double a, b;
        a = 20; b = 10;
        System.out.println("Сумма модулей переменных равна " + Math.abs(a + b));
        System.out.println("Разность модулей переменных равна " + Math.abs(a - b));
        System.out.println("Произведение модулей переменных равно " + Math.abs(a * b));
        System.out.println("Частное модулей переменных равно " + Math.abs(a / b));
    }
}
